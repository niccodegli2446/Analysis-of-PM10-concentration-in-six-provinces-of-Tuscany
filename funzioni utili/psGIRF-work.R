    ci <- 1 - ci
    BOOT <- .boot.girf(x = x, n.ahead = n.ahead, runs = runs, ortho = ortho, 
      cumulative = cumulative, impulse = impulse, response = response, 
      ci = ci, seed = seed, y.names = y.names)
    Lower <- BOOT$Lower
