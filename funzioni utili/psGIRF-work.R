  result <- list(irf=irs, Lower=Lower, Upper=Upper, response=response, 
    impulse=impulse, ortho=ortho, cumulative=cumulative, 
    runs=runs, ci=ci, boot=boot, model = class(x))
  class(result) <- "varirf"
  return(result)

}
