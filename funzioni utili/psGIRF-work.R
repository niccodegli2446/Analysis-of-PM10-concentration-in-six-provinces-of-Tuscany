  ## Getting the irf
  # irs <- .irf(x = x, impulse = impulse, response = response, y.names = y.names, 
  #   n.ahead = n.ahead, ortho = ortho, cumulative = cumulative)
  irs <- .girf(x = x, impulse = impulse, response = response, y.names = y.names, 
    n.ahead = n.ahead, ortho = ortho, cumulative = cumulative)
  girs.g <<- irs
  
  ## Bootstrapping
  Lower <- NULL
  Upper <- NULL
  if(boot){
